/**
 * Generates docs/shortcuts.md from frontend/src/shortcuts/manifest.ts.
 * Run via: npm run docs:shortcuts
 *
 * Uses esbuild to bundle the TS manifest to a temp ESM file, then imports it.
 */

import { build } from 'esbuild';
import { writeFileSync, mkdirSync, rmSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { tmpdir } from 'os';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const MANIFEST_SRC = join(ROOT, 'frontend/src/shortcuts/manifest.ts');
const OUT_DIR = join(ROOT, 'docs');
const OUT_FILE = join(OUT_DIR, 'shortcuts.md');

const TMP_DIR = join(tmpdir(), 'mythos-shortcuts-gen');
const TMP_FILE = join(TMP_DIR, 'manifest.mjs');

mkdirSync(TMP_DIR, { recursive: true });
mkdirSync(OUT_DIR, { recursive: true });

// Bundle the TS manifest to a plain ESM module so we can import it at runtime.
await build({
  entryPoints: [MANIFEST_SRC],
  outfile: TMP_FILE,
  bundle: true,
  format: 'esm',
  platform: 'node',
  target: 'node18',
});

const { default: MANIFEST } = await import(TMP_FILE);

rmSync(TMP_DIR, { recursive: true, force: true });

// ─── Helpers ─────────────────────────────────────────────────────────────────

function comboLabel(combo) {
  const parts = [];
  if (combo.ctrlOrCmd) parts.push('Ctrl / Cmd');
  if (combo.shift) parts.push('Shift');
  if (combo.alt) parts.push('Alt');
  const key = keyDisplayName(combo.key);
  parts.push(key);
  return parts.join(' + ');
}

function keyDisplayName(key) {
  const MAP = {
    ' ': 'Space',
    'ArrowUp': '↑',
    'ArrowDown': '↓',
    'ArrowLeft': '←',
    'ArrowRight': '→',
    'Escape': 'Esc',
    'Enter': 'Enter',
    'Tab': 'Tab',
    'Backspace': 'Backspace',
    'Delete': 'Delete',
    'Home': 'Home',
    'End': 'End',
  };
  return MAP[key] ?? key;
}

function keysLabel(keys) {
  return keys.map(comboLabel).join(' or ');
}

const GROUP_TITLES = {
  modes: 'Writing Modes',
  navigation: 'Navigation',
  vault: 'Vault & File Tree',
  editor: 'Editor',
  brainstorm: 'Brainstorm & Writing Assistant',
  dialogs: 'Dialogs',
  help: 'Help',
};

const SCOPE_LABELS = {
  global: 'Global',
  editor: 'Editor',
  list: 'List / Panel',
  dialog: 'Dialog',
  tree: 'File Tree',
};

// ─── Render ──────────────────────────────────────────────────────────────────

const groups = {};
for (const entry of MANIFEST) {
  if (!groups[entry.group]) groups[entry.group] = [];
  groups[entry.group].push(entry);
}

const lines = [
  '# Keyboard Shortcuts',
  '',
  '> **Source of truth:** `frontend/src/shortcuts/manifest.ts`  ',
  '> This file is generated by `npm run docs:shortcuts` — do not edit by hand.',
  '',
  '## Contents',
  '',
  ...Object.keys(GROUP_TITLES).filter(g => groups[g]).map(g => `- [${GROUP_TITLES[g]}](#${g})`),
  '',
  '---',
  '',
];

for (const [group, title] of Object.entries(GROUP_TITLES)) {
  const entries = groups[group];
  if (!entries) continue;

  lines.push(`## ${title} {#${group}}`);
  lines.push('');
  lines.push('| Shortcut | Action | Scope | Notes |');
  lines.push('|----------|--------|-------|-------|');

  for (const e of entries) {
    const keys = `\`${keysLabel(e.keys)}\``;
    const scope = SCOPE_LABELS[e.scope] ?? e.scope;
    const notes = e.whenDisabled ? `_${e.whenDisabled}_` : '';
    lines.push(`| ${keys} | ${e.label} | ${scope} | ${notes} |`);
  }

  lines.push('');
}

lines.push('---');
lines.push('');
lines.push('## Intentionally Absent Shortcuts');
lines.push('');
lines.push('The following common shortcuts are **not** wired in Mythos Writer by design:');
lines.push('');
lines.push('| Chord | Reason |');
lines.push('|-------|--------|');
lines.push('| `Ctrl / Cmd + S` | Autosave is always on — explicit save is unnecessary. |');
lines.push('| `Ctrl / Cmd + N` | New note is toolbar-only to prevent accidental creation. |');
lines.push('| `Ctrl / Cmd + F` | Global find relies on focusing the SearchBar (no modal). |');
lines.push('');
lines.push('---');
lines.push('');
lines.push(`_Generated ${new Date().toISOString().split('T')[0]} from manifest.ts — ${MANIFEST.length} entries_`);
lines.push('');

writeFileSync(OUT_FILE, lines.join('\n'), 'utf-8');
console.log(`✓ Written ${MANIFEST.length} shortcuts to ${OUT_FILE}`);
